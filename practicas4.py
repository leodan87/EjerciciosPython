# Una tienda ofrece un descuento del 12% sobre el total de la compra y un cliente desea saber
#cuanto debera pagar finalmente por su compra
precio = float(input('Digite el valor de la compra :'))
descuento = precio * 0.12
precioFinal = precio - descuento
print ('El precio final a pagar es de $:',precioFinal)